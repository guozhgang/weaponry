package com.cce.weaponry.web.vo.trace;

import java.util.Date;

import com.cce.weaponry.web.vo.BaseVO;

public class EventVO extends BaseVO {
	private String entName;// 企业
	private String eventName;// 处理
	private Date recordDT;// 时间
	private String location;// 地点
	private String operator;// 处理人
	private String temperatureScope; // 温度范围
	private String humidityScope; // 温度范围
	private String status;// 状态

	public String getEntName() {
		return entName;
	}

	public void setEntName(String entName) {
		this.entName = entName;
	}

	public String getEventName() {
		return eventName;
	}

	public void setEventName(String eventName) {
		this.eventName = eventName;
	}

	public Date getRecordDT() {
		return recordDT;
	}

	public void setRecordDT(Date recordDT) {
		this.recordDT = recordDT;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

	public String getTemperatureScope() {
		return temperatureScope;
	}

	public void setTemperatureScope(String temperatureScope) {
		this.temperatureScope = temperatureScope;
	}

	public String getHumidityScope() {
		return humidityScope;
	}

	public void setHumidityScope(String humidityScope) {
		this.humidityScope = humidityScope;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
