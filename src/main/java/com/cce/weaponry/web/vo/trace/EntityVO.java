package com.cce.weaponry.web.vo.trace;

import com.cce.weaponry.web.vo.BaseVO;

public class EntityVO extends BaseVO {
	private String traceCode;// 溯源码
	private String region;// 产地 ENTITY 表对应的EVENT第一条或屠宰阶段的一条所在地区
	private String butcher;// 生产
	private String inventory;// 配送
	private String transport;// 运输
	private String sell;// 销售
	private String sold;// 售出
	private String status;// 状态

	public String getTraceCode() {
		return traceCode;
	}

	public void setTraceCode(String traceCode) {
		this.traceCode = traceCode;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getButcher() {
		return butcher;
	}

	public void setButcher(String butcher) {
		this.butcher = butcher;
	}

	public String getInventory() {
		return inventory;
	}

	public void setInventory(String inventory) {
		this.inventory = inventory;
	}

	public String getTransport() {
		return transport;
	}

	public void setTransport(String transport) {
		this.transport = transport;
	}

	public String getSell() {
		return sell;
	}

	public void setSell(String sell) {
		this.sell = sell;
	}

	public String getSold() {
		return sold;
	}

	public void setSold(String sold) {
		this.sold = sold;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
