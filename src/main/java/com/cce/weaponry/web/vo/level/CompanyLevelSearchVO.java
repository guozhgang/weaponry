package com.cce.weaponry.web.vo.level;

import java.util.Date;

import com.cce.weaponry.web.vo.BaseVO;

public class CompanyLevelSearchVO extends BaseVO {
	private String entname;
	private String entLevel;
	private Date beginDate;
	private Date endDate;
	private String regionId;
	private Integer status;
	private Long fileId;
	private String levelState;

	/**
	 * @return the levelState
	 */
	public String getLevelState() {
		return levelState;
	}

	/**
	 * @param levelState
	 *            the levelState to set
	 */
	public void setLevelState(String levelState) {
		this.levelState = levelState;
	}

	public Long getFileId() {
		return fileId;
	}


	public void setFileId(Long fileId) {
		this.fileId = fileId;
	}

	public CompanyLevelSearchVO(String entname, String entLevel,
			Date beginDate, Date endDate, String regionId, Integer status,
			Long fileId) {
		super();
		this.entname = entname;
		this.entLevel = entLevel;
		this.beginDate = beginDate;
		this.endDate = endDate;
		this.regionId = regionId;
		this.status = status;
		this.fileId = fileId;
	}

	public CompanyLevelSearchVO() {
		super();
		// TODO Auto-generated constructor stub
	}


	public String getEntname() {
		return entname;
	}

	public void setEntname(String entname) {
		this.entname = entname;
	}

	public String getEntLevel() {
		return entLevel;
	}

	public void setEntLevel(String entLevel) {
		this.entLevel = entLevel;
	}

	public Date getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getRegionId() {
		return regionId;
	}

	public void setRegionId(String regionId) {
		this.regionId = regionId;
	}


	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}



}
