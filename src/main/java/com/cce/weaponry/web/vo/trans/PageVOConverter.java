package com.cce.weaponry.web.vo.trans;

import com.cce.modules.orm.Page;

public class PageVOConverter extends BaseVDOConverter {

	public Page convert(Page entity) {
		Page vo = new Page();
		vo.setOrder(entity.getOrder());
		vo.setOrderBy(entity.getOrderBy());
		vo.setPageNo(entity.getPageNo());
		vo.setPageSize(entity.getPageSize());
		vo.setTotalCount(entity.getTotalCount());
		return vo;
	}

}
