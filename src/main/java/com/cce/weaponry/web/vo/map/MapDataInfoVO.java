package com.cce.weaponry.web.vo.map;

import com.cce.weaponry.web.vo.BaseVO;

public class MapDataInfoVO extends BaseVO {
	private String regionName;
	private boolean fetchEnt;
	private boolean fetchInno;
	private boolean fetchTech;
	private Long entDistNum;
	private Long innoDistNum;
	private Long techDistNum;

	public boolean isFetchEnt() {
		return fetchEnt;
	}

	public void setFetchEnt(boolean fetchEnt) {
		this.fetchEnt = fetchEnt;
	}

	public boolean isFetchInno() {
		return fetchInno;
	}

	public void setFetchInno(boolean fetchInno) {
		this.fetchInno = fetchInno;
	}

	public boolean isFetchTech() {
		return fetchTech;
	}

	public void setFetchTech(boolean fetchTech) {
		this.fetchTech = fetchTech;
	}

	public String getRegionName() {
		return regionName;
	}

	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}

	public Long getEntDistNum() {
		return entDistNum;
	}

	public void setEntDistNum(Long entDistNum) {
		this.entDistNum = entDistNum;
	}

	public Long getInnoDistNum() {
		return innoDistNum;
	}

	public void setInnoDistNum(Long innoDistNum) {
		this.innoDistNum = innoDistNum;
	}

	public Long getTechDistNum() {
		return techDistNum;
	}

	public void setTechDistNum(Long techDistNum) {
		this.techDistNum = techDistNum;
	}

}
